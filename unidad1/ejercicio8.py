"""
    Un trabajador cobra $2500 por hora. Si el usuario ingresa la cantidad de horas trabajadas, calcular el sueldo
    total.
"""

cantidadHoras = int(input("Ingrese la cantidad de horas trabajadas: "));
valorHora = 2500;
sueldoBruto = cantidadHoras * valorHora;
print("El sueldo bruto del empleado es: ", sueldoBruto);
