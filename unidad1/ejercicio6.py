"""
    Se desea convertir una temperatura ingresada en grados Celsius a grados Fahrenheit. La formula es: F = (C x 9/5) +32
    Mostrar el resultado.
"""

temperaturaCelsius = float(input("Ingrese la temperatura en grados Celsius: "));
temperaturaFahrenheit = (temperaturaCelsius * 9/5) + 32; """ Calcular la temperatura en grados Fahrenheit utilizando la fórmula de conversión """
print("La temperatura en grados Fahrenheit es: ", temperaturaFahrenheit); 